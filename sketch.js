function createElement(jsonInput) {
    //Set background color and create item div!
    let newContentElement = document.createElement("DIV");
    newContentElement.style.backgroundColor = jsonInput['color'];
    newContentElement.style.color = jsonInput['text_color'];
    newContentElement.classList.add('contentItem');

    //Set name
    let newContentName = document.createElement("H2");
    newContentName.classList.add('contentName');
    newContentName.innerHTML = jsonInput['name'];
    newContentElement.appendChild(newContentName);

    //Set game
    let newContentGame = document.createElement("H3");
    newContentGame.classList.add('contentGame');
    newContentGame.innerHTML = "Game: " + jsonInput['game'];
    newContentElement.appendChild(newContentGame);

    //Set city
    let newContentCity = document.createElement("H3");
    newContentCity.classList.add('contentCity');
    newContentCity.innerHTML = "City: " + jsonInput['city'];
    newContentElement.appendChild(newContentCity);

    //create image
    let newImage = document.createElement("IMG");
    newImage.classList.add("key_img");
    newImage.src = jsonInput['picture'];
    newContentElement.appendChild(newImage);

    //Set image song
    let newContentSong = document.createElement("H3");
    newContentSong.classList.add('contentSong');
    newContentSong.innerHTML = "Image Song: " + jsonInput['image_song'];
    newContentElement.appendChild(newContentSong);


    //add stuff to page
    contentGrid.appendChild(newContentElement);

}

